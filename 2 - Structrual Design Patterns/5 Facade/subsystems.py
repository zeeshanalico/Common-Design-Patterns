class FlightBooking:
    def book_flight(self, destination: str) -> str:
        return f"Flight booked to {destination}"

class HotelBooking:
    def book_hotel(self, destination: str) -> str:
        return f"Hotel booked in {destination}"

class CarRental:
    def book_car(self, destination: str) -> str:
        return f"Car rented in {destination}"

